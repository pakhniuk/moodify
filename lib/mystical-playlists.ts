// Get a random song from a specific mood playlist
export const getRandomSongFromMood = (mood: string) => {
  const playlist = mysticalPlaylists[mood as keyof typeof mysticalPlaylists];
  if (!playlist || playlist.length === 0) {
    // Fallback to Energizing if mood not found
    const fallbackPlaylist = mysticalPlaylists.Energizing;
    return fallbackPlaylist[Math.floor(Math.random() * fallbackPlaylist.length)];
  }
  
  return playlist[Math.floor(Math.random() * playlist.length)];
};

// Get all available moods
export const getAvailableMoods = () => {
  return Object.keys(mysticalPlaylists);
};

// Get song count for each mood
export const getPlaylistStats = () => {
  const stats: { [key: string]: number } = {};
  Object.entries(mysticalPlaylists).forEach(([mood, songs]) => {
    stats[mood] = songs.length;
  });
  return stats;
};
